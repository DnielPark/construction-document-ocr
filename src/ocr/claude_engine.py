"""
Claude Vision API 엔진
"""

import anthropic
import base64
import json
from pathlib import Path


class ClaudeEngine:
    """Claude Vision API 클래스"""
    
    def __init__(self, api_key):
        self.client = anthropic.Anthropic(api_key=api_key)
        print("Claude Vision 엔진 초기화 완료!")
        
    def extract_table(self, image_path):
        """이미지에서 표 데이터 추출"""
        # Path 객체를 문자열로 변환
        image_path_str = str(image_path)
        print(f"📊 Claude Vision으로 표 추출 중: {Path(image_path_str).name}")
        
        # 이미지를 base64로 인코딩
        with open(image_path_str, 'rb') as f:
            image_data = base64.standard_b64encode(f.read()).decode('utf-8')
        
        # 이미지 확장자 확인
        ext = image_path_str.lower().split('.')[-1]
        media_type = f"image/{ext}" if ext in ['png', 'jpg', 'jpeg'] else "image/jpeg"
        
        prompt = """첨부된 이미지는 하수관로 공사 수량표입니다.

═══════════════════════════════════════════════════════════
1. 표의 구조
═══════════════════════════════════════════════════════════

┌────────┬─────────┬───────┬──────────┬─────────┐
│ 측점 │ 20+0.000 │
├────────┼─────────┼───────┼──────────┼─────────┤
│ 지반고 │ 11.40m │ 파취 │ A.S.P │ 1.72m │
├────────┼─────────┤ 및 ├──────────┼─────────┤
│ 관저고 │ 10.31m │ 복구 │ Con'C │ m │
├────────┼─────────┤ ├──────────┼─────────┤
│터파기 │ 육상 │ 1.25㎡│ │ 보도블럭 │ m │
│ ├─────────┼───────┼──────────┴─────────┤
│ │ 수중 │ 0.00㎡│ 모래부설 │ 0.33㎡ │
├────────┼─────────┼───────┼───────────┼─────────┤
│되메우기│ 관주위 │ 0.00㎡│ 보조기층 │ 0.47㎡ │
│ ├─────────┼───────┼───────────┼─────────┤
│ │ 관상단 │ 0.45㎡│ 동상방지층│ 0.00㎡ │
└────────┴─────────┴───────┴───────────┴─────────┘

═══════════════════════════════════════════════════════════
2. 추출 방법: 라벨 + 단위 패턴 찾기
═══════════════════════════════════════════════════════════

각 항목마다 [라벨 텍스트] 근처에서 [숫자][단위]를 찾으세요.

【1. 측점】
찾기: "측점", "No", "No." 근처
형식: 숫자+숫자.숫자 (예: 20+0.000, 22+12.200)
단위: 없음

【2. 지반고】
찾기: "지반고", "지 반 고"
단위: m
예시: 11.40m → "11.40"

【3. 관저고】
찾기: "관저고", "관 저 고"
단위: m
예시: 10.31m → "10.31"

【4. 터파기 육상】
찾기: "터파기", "육상", "측상"
단위: ㎡ 또는 m²
예시: 1.25㎡ → "1.25"

【5. 터파기 수중】
찾기: "터파기", "수중"
단위: ㎡ 또는 m²
예시: 0.00㎡ → "0.00"

【6. 되메우기 관주위】
찾기: "되메우기", "되미우기", "관주위", "관 주 위"
단위: ㎡ 또는 m²
예시: 0.00㎡ → "0.00"

【7. 되메우기 관상단】
찾기: "되메우기", "관상단", "관 상 단"
단위: ㎡ 또는 m²
예시: 0.45㎡ → "0.45"

【8. 파취 및 복구 ASP】
찾기: "파취" 영역에서 "A.S.P" 또는 "ASP"
단위: m
⚠️ "A.S.P" 라벨 바로 옆(오른쪽)의 값만 추출
예시: A.S.P 1.72m → "1.72"

【9. 파취 및 복구 Con'c】
찾기: "파취" 영역에서 "Con'C" 또는 "ConC"
단위: m
⚠️ "Con'C" 라벨 바로 옆(오른쪽)의値만 추출
값이 없으면 "0"
예시: Con'C 0m → "0" 또는 Con'C m → "0"

【10. 파취 및 복구 보도블럭】
찾기: "파취" 영역에서 "보도블럭" 또는 "보도"
단위: m
⚠️ 중요: "보도블럭" 라벨 바로 옆(오른쪽)의 값만 추출
⚠️ "보도블럭" 아래에 있는 "모래부설" 값을 가져오면 안 됨!
값이 없으면 "0"
예시: 보도블럭 0.33m → "0.33" 또는 보도블럭 m → "0"

【11. 모래부설】
찾기: "모래", "모 래", "부설", "부 설"
단위: ㎡ 또는 m²
⚠️ "파취" 영역과 분리된 별도 항목
예시: 모래부설 0.33㎡ → "0.33"

【12. 보조기층】
찾기: "보조", "보 조", "기층", "기 층"
단위: ㎡ 또는 m²
예시: 보조기층 0.47㎡ → "0.47"

【13. 동상방지층】
찾기: "동상", "동 상", "방지", "방 지", "방지층"
단위: ㎡ 또는 m²
예시: 동상방지층 0.00㎡ → "0.00"

═══════════════════════════════════════════════════════════
3. 핵심 원칙
═══════════════════════════════════════════════════════════

1. 각 라벨과 같은 행(가로줄)에 있는 값만 추출
2. 라벨 바로 옆(오른쪽 또는 아래)에서 단위와 함께 숫자 찾기
3. 다른 항목의 값을 가져오지 말 것
4. 띄어쓰기 무시 (예: "지 반 고" = "지반고")
5. OCR 오류 수정 (예: "O" → "0", "l" → "1")

═══════════════════════════════════════════════════════════
4. 출력 형식 (JSON)
═══════════════════════════════════════════════════════════

[
  {
    "측점": "20+0.000",
    "지반고": "11.40",
    "관저고": "10.31",
    "터파기 육상": "1.25",
    "터파기 수중": "0.00",
    "되메우기 관주위": "0.00",
    "되메우기 관상단": "0.45",
    "파취 및 복구 ASP": "1.72",
    "파취 및 복구 Con'c": "0",
    "파취 및 복구 보도블럭": "0",
    "모래부설": "0.33",
    "보조기층": "0.47",
    "동상방지층": "0.00"
  }
]

규칙:
- 단위 제거 (m, ㎡, ㎥)
- 값이 없으면 "0"
- 이미지의 모든 측점 행 포함
- JSON만 출력 (설명 없이)
"""
        
        message = self.client.messages.create(
            model="claude-haiku-4-5",
            max_tokens=4096,
            messages=[
                {
                    "role": "user",
                    "content": [
                        {
                            "type": "image",
                            "source": {
                                "type": "base64",
                                "media_type": media_type,
                                "data": image_data,
                            },
                        },
                        {
                            "type": "text",
                            "text": prompt
                        }
                    ],
                }
            ],
        )
        
        content = message.content[0].text
        
        # JSON 파싱
        try:
            if '```json' in content:
                content = content.split('```json')[1].split('```')[0]
            elif '```' in content:
                content = content.split('```')[1].split('```')[0]
            
            table_data = json.loads(content.strip())
            print(f"✅ {len(table_data)}개 측점 추출 완료")
            return table_data
        except json.JSONDecodeError as e:
            raise Exception(f"JSON 파싱 오류: {e}\n응답: {content}")
            
    def extract_table_from_array(self, image_array):
        """OpenCV 배열에서 표 데이터 추출"""
        import cv2
        import tempfile
        
        # 임시 파일로 저장
        with tempfile.NamedTemporaryFile(suffix='.jpg', delete=False) as tmp:
            cv2.imwrite(tmp.name, image_array)
            tmp_path = tmp.name
        
        try:
            result = self.extract_table(tmp_path)
            return result
        finally:
            # 임시 파일 삭제
            import os
            os.unlink(tmp_path)